package com.egoveris.te.base.model;

public class SubprocessAppDTO {

  private String stateName;
  private String subprocessCode;
  private String stateFlow;
  private String procedureType;
  private String lockType;
  private String subprocessType;

  /**
   * @return the stateName
   */
  public String getStateName() {
    return stateName;
  }

  /**
   * @param stateName
   *          the stateName to set
   */
  public void setStateName(String stateName) {
    this.stateName = stateName;
  }

  /**
   * @return the subprocessCode
   */
  public String getSubprocessCode() {
    return subprocessCode;
  }

  /**
   * @param subprocessCode
   *          the subprocessCode to set
   */
  public void setSubprocessCode(String subprocessCode) {
    this.subprocessCode = subprocessCode;
  }

  /**
   * @return the stateFlow
   */
  public String getStateFlow() {
    return stateFlow;
  }

  /**
   * @param stateFlow
   *          the stateFlow to set
   */
  public void setStateFlow(String stateFlow) {
    this.stateFlow = stateFlow;
  }

  /**
   * @return the procedureType
   */
  public String getProcedureType() {
    return procedureType;
  }

  /**
   * @param procedureType
   *          the procedureType to set
   */
  public void setProcedureType(String procedureType) {
    this.procedureType = procedureType;
  }

  /**
   * @return the lockType
   */
  public String getLockType() {
    return lockType;
  }

  /**
   * @param lockType
   *          the lockType to set
   */
  public void setLockType(String lockType) {
    this.lockType = lockType;
  }

  /**
   * @return the subprocessType
   */
  public String getSubprocessType() {
    return subprocessType;
  }

  /**
   * @param subprocessType
   *          the subprocessType to set
   */
  public void setSubprocessType(String subprocessType) {
    this.subprocessType = subprocessType;
  }

}
