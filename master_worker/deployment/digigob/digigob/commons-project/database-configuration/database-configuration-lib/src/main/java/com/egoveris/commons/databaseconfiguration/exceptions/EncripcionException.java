package com.egoveris.commons.databaseconfiguration.exceptions;

public class EncripcionException extends Exception{
	

	/**
	 * 
	 */
	private static final long serialVersionUID = -7016429779497685085L;

	public EncripcionException() {
	}

	public EncripcionException(String message) {
		super(message);
	}

	public EncripcionException(Throwable cause) {
		super(cause);
	}

	public EncripcionException(String message, Throwable cause) {
		super(message, cause);
	}


}
